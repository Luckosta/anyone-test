<script setup lang="ts">
import { computed, ref } from 'vue';
import { useRoute } from 'vue-router';
import { store } from '@/store';
import InputNumber from './InputNumber.vue';

const route = useRoute();

const inputFocused = ref(false);

const person = computed(() => {
    const id = Number(route.params.id);
    return store.people.find((p) => p.id === id);
});
</script>

<template>
    <div v-if="person" class="flex flex-col gap-4">
        <router-link to="/" class="text-violet-600 hover:underline text-sm"
            >&larr; Back</router-link
        >

        <div class="flex items-center gap-3">
            <img
                src="/cat.png"
                :alt="person.name"
                class="w-14 h-14 rounded-full object-cover"
                :class="{
                    'border-2 border-violet-500': inputFocused,
                    'border-0': !inputFocused,
                }"
            />
            <div>
                <label
                    for="hours-input"
                    class="block text-sm font-bold tracking-wide text-gray-700"
                >
                    {{ person.name.toUpperCase() }} IS
                </label>
                <div class="flex items-center gap-2">
                    <InputNumber
                        v-model="person.ageInHours"
                        v-model:isFocused="inputFocused"
                    />
                    <span class="text-gray-600">hours old</span>
                </div>
            </div>
        </div>
    </div>

    <div v-else>
        <p class="text-gray-600">Person not found</p>
        <router-link to="/" class="text-violet-600 hover:underline text-sm"
            >Back to list</router-link
        >
    </div>
</template>
